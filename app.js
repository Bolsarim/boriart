function abrirtigre() {
    // Abrir a página queijo.html em uma nova janela ou guia
    window.open('tigre.html', '_blank');
    // Fechar a janela ou guia atual (pode não funcionar em alguns navegadores devido a restrições de segurança)
    window.close();
}
